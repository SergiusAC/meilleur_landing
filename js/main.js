var headerPointer = 0;
var forWhom = 'man';
var mainAssortmentPointer = 0;

function setHeaderOrderInfo(point) {
    if (point >= 0 && point <= 9) {
        $('.header-order__title p').text(aromas['woman'][point].title);
        $('.header-order__image img').attr('src', aromas['woman'][point].image_url);
    } else if (point >= 10 && point <= 19) {
        $('.header-order__title p').text(aromas['man'][point - 10].title);
        $('.header-order__image img').attr('src', aromas['man'][point - 10].image_url);
    }
}

function setMainAssortmentInfo(point, forWhom, isMobile) {
    if (isMobile) {
        $('.aromas-m-info-block__title h4').text(aromas[forWhom][point].title);
        $('.aromas-m-info-block__desc p').text(aromas[forWhom][point].desc);
        $('.aromas-m-info-block__image img').attr('src', aromas[forWhom][point].image_url);
    } else {
        $('.info-block__title h4').text(aromas[forWhom][point].title);
        $('.info-block__desc p').text(aromas[forWhom][point].desc);
        $('.aromas-info__image img').attr('src', aromas[forWhom][point].image_url);
    }
}

$(document).ready(function () {

    setHeaderOrderInfo(headerPointer);

    $('.header-order__row_left img').on('click', function () {
        headerPointer--;
        if (headerPointer < 0) {
            headerPointer = 19;
        }
        setHeaderOrderInfo(headerPointer);
    });

    $('.header-order__row_right img').on('click', function () {
        headerPointer = (headerPointer + 1) % 20;
        setHeaderOrderInfo(headerPointer);
    });

    $('.aromas-man .aromas__btn button').on('click', function () {
        forWhom = 'man';
        mainAssortmentPointer = 0;
        setMainAssortmentInfo(mainAssortmentPointer, forWhom, false);
    })

    $('.aromas-woman .aromas__btn button').on('click', function () {
        forWhom = 'woman';
        mainAssortmentPointer = 0;
        setMainAssortmentInfo(mainAssortmentPointer, forWhom, false);
    })

    $('.aromas-info__row_left img').on('click', function () {
        mainAssortmentPointer--;
        if (mainAssortmentPointer < 0) {
            mainAssortmentPointer = 9;
        }
        setMainAssortmentInfo(mainAssortmentPointer, forWhom, false);
    });

    $('.aromas-info__row_right img').on('click', function () {
        mainAssortmentPointer = (mainAssortmentPointer + 1) % 10;
        setMainAssortmentInfo(mainAssortmentPointer, forWhom, false);
    });

    $('.aromas-m-btns-man button').on('click', function () {
        forWhom = 'man';
        mainAssortmentPointer = 0;
        setMainAssortmentInfo(mainAssortmentPointer, forWhom, true);
    });

    $('.aromas-m-btns-woman button').on('click', function () {
        forWhom = 'woman';
        mainAssortmentPointer = 0;
        setMainAssortmentInfo(mainAssortmentPointer, forWhom, true);
    });

    $('.aromas-m-info__row_left img').on('click', function () {
        mainAssortmentPointer--;
        if (mainAssortmentPointer < 0) {
            mainAssortmentPointer = 9;
        }
        setMainAssortmentInfo(mainAssortmentPointer, forWhom, true);
    });

    $('.aromas-m-info__row_right img').on('click', function () {
        mainAssortmentPointer = (mainAssortmentPointer + 1) % 10;
        setMainAssortmentInfo(mainAssortmentPointer, forWhom, true);
    });

    $('.aromas-m-info-block__desc span').on('click', function () {
        if ($('.aromas-m-info-block__desc_toggle')[0].hidden) {
            $('.aromas-m-info-block__desc span').text('Скрыть описание');
            $('.aromas-m-info-block__desc_toggle')[0].hidden = false;
        } else {
            $('.aromas-m-info-block__desc span').text('Показать описание');
            $('.aromas-m-info-block__desc_toggle')[0].hidden = true;
        }
    })
});